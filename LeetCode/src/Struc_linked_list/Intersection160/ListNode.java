package Struc_linked_list.Intersection160;

public class ListNode {
    int val;
    ListNode next;
    public ListNode(){}
    public ListNode(int val){
        this.val = val;
        this.next = null;
    }
    public int getVal(){
        return this.val;
    }
    public void setVal(int val){
        this.val = val;
    }
}
